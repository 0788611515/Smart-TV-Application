function App() {
  const mainDivRef = React.createRef<HTMLDivElement>();
  const [mobileOpen, setMobileOpen] = React.useState(false);

  const [page, setPage] = useState(0);
  const [topic, setTopic] = useState('');

  const { topics } = useGetTopics();
  const { photos } = useGetPhotos(topic, page);

  const handleDrawerToggle = () => {
    setMobileOpen(!mobileOpen);
  };

  const drawer = (
    <div>
      <Toolbar />
      <Divider />
      <List>
        {topics.map(_topic => (
          <ListItem key={_topic.id} disablePadding>
            <ListItemButton selected={_topic.id === topic} onClick={() => setTopic(_topic.id)}>
              <ListItemText primary={_topic.title} />
            </ListItemButton>
          </ListItem>
        ))}
      </List>
    </div>
  );

  const handleScrollRight = () => {
    const currentPos = mainDivRef?.current?.scrollLeft;
    mainDivRef?.current?.scroll({ left: Number(currentPos!) + SCROlL_THRESHOLDS, behavior: 'smooth' });
  }

  const handleScrollLeft = () => {
    const currentPos = mainDivRef?.current?.scrollLeft;
    mainDivRef?.current?.scroll({ left: Number(currentPos!) - SCROlL_THRESHOLDS, behavior: 'smooth' });
  }

  return (
    <Box sx={{ display: 'flex' }}>
      <CssBaseline />
      <AppBar
        position="fixed"
        sx={{
          width: { sm: `calc(100% - ${drawerWidth}px)` },
          ml: { sm: `${drawerWidth}px` },
        }}
      >
        <Toolbar>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            edge="start"
            onClick={handleDrawerToggle}
            sx={{ mr: 2, display: { sm: 'none' } }}
          >

          </IconButton>
          <Typography variant="h6" noWrap component="div">
            Smart TV App
          </Typography>
        </Toolbar>
      </AppBar>
      <Box
        component="nav"
        sx={{ width: { sm: drawerWidth }, flexShrink: { sm: 0 } }}
        aria-label="mailbox folders"
      >
        {/* The implementation can be swapped with js to avoid SEO duplication of links. */}
        <Drawer
          variant="temporary"
          open={mobileOpen}
          onClose={handleDrawerToggle}
          ModalProps={{
            keepMounted: true, // Better open performance on mobile.
          }}
          sx={{
            display: { xs: 'block', sm: 'none' },
            '& .MuiDrawer-paper': { boxSizing: 'border-box', width: drawerWidth },
          }}
        >
          {drawer}
        </Drawer>
        <Drawer
          variant="permanent"
          sx={{
            display: { xs: 'none', sm: 'block' },
            '& .MuiDrawer-paper': { boxSizing: 'border-box', width: drawerWidth },
          }}
          open
        >
          {drawer}
        </Drawer>
      </Box>
      <Box component="main" sx={{ flexGrow: 1, p: 3, width: { sm: `calc(100% - ${drawerWidth}px)` } }}>
        <Toolbar />
        <button onClick={handleScrollLeft}>Previous</button>
        <button onClick={handleScrollRight}>Next</button>
        <div className="grid" ref={mainDivRef}>
          {photos.map(photo => (
            <Zoom key={photo.id} zoomImg={{ src: photo.urls.raw }}>
              <img
                src={photo.urls.thumb}
                loading="lazy"
                className="img"
                alt={photo.description}
              />
            </Zoom>
          ))}
        </div>
      </Box>
    </Box>
  );
}

export default App
